  const RTL_LANGS = ['ar', 'he'];
  const DEFAULT_LANG = 'en';

  const THEME_KEY = 'theme';
  const LANG_KEY = 'lang';
  const root = document.documentElement;
  const themeButtons = document.querySelectorAll('[data-theme-set]');
  const langSelect = document.getElementById('lang-select');
  const themeSwitch = document.querySelector('.theme-switch');
  const metaDescription = document.querySelector('meta[name="description"]');

  function applyLanguage(lang) {
    const strings = TRANSLATIONS[lang] || TRANSLATIONS[DEFAULT_LANG];
    const activeLang = TRANSLATIONS[lang] ? lang : DEFAULT_LANG;

    root.lang = activeLang;
    root.dir = RTL_LANGS.indexOf(activeLang) !== -1 ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      const key = el.getAttribute('data-i18n');
      if (strings[key]) {
        el.textContent = strings[key];
      }
    });

    document.querySelectorAll('[data-i18n-alt]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-alt');
      if (strings[key]) {
        el.alt = strings[key];
      }
    });

    if (metaDescription && strings.metaDescription) {
      metaDescription.setAttribute('content', strings.metaDescription);
    }

    if (strings.pageTitle) {
      document.title = strings.pageTitle;
    }

    if (themeSwitch && strings.ariaTheme) {
      themeSwitch.setAttribute('aria-label', strings.ariaTheme);
    }

    if (langSelect && strings.ariaLang) {
      langSelect.setAttribute('aria-label', strings.ariaLang);
    }

    if (langSelect) {
      langSelect.value = activeLang;
    }

    localStorage.setItem(LANG_KEY, activeLang);
  }

  if (langSelect) {
    langSelect.addEventListener('change', function () {
      applyLanguage(langSelect.value);
    });

    const savedLang = localStorage.getItem(LANG_KEY);
    if (savedLang && TRANSLATIONS[savedLang]) {
      applyLanguage(savedLang);
    } else {
      applyLanguage(DEFAULT_LANG);
    }
  }

  function setTheme(theme) {
    const isDark = theme === 'dark';

    if (isDark) {
      root.setAttribute('data-theme', 'dark');
    } else {
      root.removeAttribute('data-theme');
    }

    localStorage.setItem(THEME_KEY, isDark ? 'dark' : 'light');

    themeButtons.forEach(function (btn) {
      const active = btn.getAttribute('data-theme-set') === theme;
      btn.classList.toggle('is-active', active);
      btn.setAttribute('aria-pressed', active ? 'true' : 'false');
    });
  }

  themeButtons.forEach(function (btn) {
    btn.addEventListener('click', function () {
      setTheme(btn.getAttribute('data-theme-set'));
    });
  });

  const savedTheme = localStorage.getItem(THEME_KEY);
  if (savedTheme === 'dark' || savedTheme === 'light') {
    setTheme(savedTheme);
  }

  const contactButtons = document.querySelectorAll('[data-scroll-to="contact"]');
  const contactSection = document.getElementById('contact');

  if (contactButtons.length && contactSection) {
    contactButtons.forEach(function (contactButton) {
      contactButton.addEventListener('click', function (event) {
        event.preventDefault();
        contactSection.scrollIntoView({
          behavior: 'smooth',
          block: 'center',
        });
        contactSection.setAttribute('tabindex', '-1');
        contactSection.focus({ preventScroll: true });
      });
    });
  }
